func punteros_avanzado(n int) {
	var sm int
	var s *int
	var ss **int
	s = &sm
	ss = &s

	for **ss = 0; n > 0; **ss *= 10 {
		**ss += n % 10
		n /= 10
	}
	sm /= 10

	fmt.Println("<sm> ==> ", sm)
	fmt.Println("<n> ==> ", n)
}
